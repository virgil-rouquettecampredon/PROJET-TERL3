package org.example.model.Regles.Structure;

import org.example.model.Regles.Fonctions_Comportements;
import org.example.model.Regles.Jeton;
import org.example.model.Regles.ObjetsDeRegle;
import org.example.model.Regles.SujetDeRegle;

import java.util.ArrayList;
import java.util.List;

public class Alias_Sujet extends Alias<Jeton>{

    private List<SujetDeRegle> sujetsDeRegle;

    public Alias_Sujet(Jeton j){
        super(j);
        sujetsDeRegle = new ArrayList<>();
    }

    public List<SujetDeRegle> getSujetsDeRegle(){return sujetsDeRegle;}

    public void editionDesLiens(){
        //Si la Condition vaut vrai
        if(conditionDeDefinition.evaluer()){
            //Liste des éléments à récupérer de la Condition
            sujetsDeRegle = new ArrayList<>(Fonctions_Comportements.sujetDeLaConditionVrai);
        }else{
            sujetsDeRegle = new ArrayList<>();
        }
    }
}
